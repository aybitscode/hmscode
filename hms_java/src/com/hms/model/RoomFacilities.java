package com.hms.model;

public class RoomFacilities {
	private String slno;
	private String room_facilities_ID;
	private String room_facilities_name;
	private String room_facilities_desc;
	private String room_facilities_price;
	public String getRoom_facilities_ID() {
		return room_facilities_ID;
	}
	public void setRoom_facilities_ID(String room_facilities_ID) {
		this.room_facilities_ID = room_facilities_ID;
	}
	public String getRoom_facilities_name() {
		return room_facilities_name;
	}
	public void setRoom_facilities_name(String room_facilities_name) {
		this.room_facilities_name = room_facilities_name;
	}
	public String getRoom_facilities_desc() {
		return room_facilities_desc;
	}
	public void setRoom_facilities_desc(String room_facilities_desc) {
		this.room_facilities_desc = room_facilities_desc;
	}
	public String getSlno() {
		return slno;
	}
	public void setSlno(String slno) {
		this.slno = slno;
	}
	public String getRoom_facilities_price() {
		return room_facilities_price;
	}
	public void setRoom_facilities_price(String room_facilities_price) {
		this.room_facilities_price = room_facilities_price;
	}

}
