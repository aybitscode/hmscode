package com.hms.model;

public class Hotel {
	private String slno;
	private String hotel_ID;
	private String hotel_name;
	private String hotel_desc;
	private String hotel_total_rooms;
	private String hotel_total_floors;
	
	public String getHotel_ID() {
		return hotel_ID;
	}
	public void setHotel_ID(String hotel_ID) {
		this.hotel_ID = hotel_ID;
	}
	public String getHotel_name() {
		return hotel_name;
	}
	public void setHotel_name(String hotel_name) {
		this.hotel_name = hotel_name;
	}
	public String getHotel_desc() {
		return hotel_desc;
	}
	public void setHotel_desc(String hotel_desc) {
		this.hotel_desc = hotel_desc;
	}
	public String getHotel_total_rooms() {
		return hotel_total_rooms;
	}
	public void setHotel_total_rooms(String hotel_total_rooms) {
		this.hotel_total_rooms = hotel_total_rooms;
	}
	public String getHotel_total_floors() {
		return hotel_total_floors;
	}
	public void setHotel_total_floors(String hotel_total_floors) {
		this.hotel_total_floors = hotel_total_floors;
	}
	public String getSlno() {
		return slno;
	}
	public void setSlno(String slno) {
		this.slno = slno;
	}

}
