package com.hms.model;

public class RoomCapacity {
	private String slno;
	private String room_capacity_ID;
	private String room_capacity_name;
	private String room_capacity_adults;
	private String room_capacity_childs;
	private String room_total_capacity;
	public String getRoom_capacity_ID() {
		return room_capacity_ID;
	}
	public void setRoom_capacity_ID(String room_capacity_ID) {
		this.room_capacity_ID = room_capacity_ID;
	}
	public String getRoom_capacity_name() {
		return room_capacity_name;
	}
	public void setRoom_capacity_name(String room_capacity_name) {
		this.room_capacity_name = room_capacity_name;
	}
	public String getRoom_capacity_adults() {
		return room_capacity_adults;
	}
	public void setRoom_capacity_adults(String room_capacity_adults) {
		this.room_capacity_adults = room_capacity_adults;
	}
	public String getRoom_capacity_childs() {
		return room_capacity_childs;
	}
	public void setRoom_capacity_childs(String room_capacity_childs) {
		this.room_capacity_childs = room_capacity_childs;
	}
	public String getSlno() {
		return slno;
	}
	public void setSlno(String slno) {
		this.slno = slno;
	}
	public String getRoom_total_capacity() {
		return room_total_capacity;
	}
	public void setRoom_total_capacity(String room_total_capacity) {
		this.room_total_capacity = room_total_capacity;
	}

}
